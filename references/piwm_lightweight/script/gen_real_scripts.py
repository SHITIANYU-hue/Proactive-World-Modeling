def write_script(entry):
    sid, content = entry
    # 用中文组别名给文件命名，不暴露技术 action code
    group_map = {
        "real_02": "开场问候",
        "real_03": "引出需求",
        "real_04": "简述区别",
        "real_05": "温和推荐",
        "real_06": "缓解纠结",
        "real_07": "静默等待",
        "real_08": "静默等待",
    }
    prefix = sid[:7]
    group = group_map.get(prefix, "")
    filename = f"{sid}_{group}.md" if group else f"{sid}.md"
    path = os.path.join(OUT_DIR, filename)
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"wrote {filename}")


