def main() -> None:
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    all_groups = [
        ("group1", GROUP1),
        ("group2", GROUP2),
        ("group5", GROUP5),
    ]
    written = []
    for _name, records in all_groups:
        for rec in records:
            sid = rec["session_id"]
            path = OUT_DIR / f"{sid}.json"
            path.write_text(json.dumps(rec, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
            written.append(sid)
            print(f"wrote {path}")

    # Update index.json
    index_path = OUT_DIR / "index.json"
    index = json.loads(index_path.read_text(encoding="utf-8"))
    index["available"] = 40
    index["groups"]["1"]["eval_files"] = [f"{s}.json" for s in written if s.startswith("real_0")]
    index["groups"]["2"]["eval_files"] = [f"{s}.json" for s in written if s.startswith("real_01") or s.startswith("real_02") and int(s.split("_")[1]) <= 20]
    # Re-derive properly
    g1 = [f"real_{i:03d}.json" for i in range(1, 11)]
    g2 = [f"real_{i:03d}.json" for i in range(11, 21)]
    g5 = [f"real_{i:03d}.json" for i in range(41, 51)]
    index["groups"]["1"]["eval_files"] = g1
    index["groups"]["2"]["eval_files"] = g2
    index["groups"]["5"]["eval_files"] = g5
    index_path.write_text(json.dumps(index, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"\nUpdated index.json — available: {index['available']}")
    print(f"Total written: {len(written)} files")


if __name__ == "__main__":
    main()
